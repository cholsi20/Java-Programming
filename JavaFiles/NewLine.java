package New_print_line;

public class NewLine {
	
public static void main(String[] args) { //begin main method argument
		
		System.out.print( "Welcome to: " ); //prints message to console 
		System.out.println( "Java Programming!" );
		System.out.println( "Welcome\nto\nJava\nProgramming!" );
		System.out.println( "Your default java folder is c:\\program files\\java\\" );
		System.out.printf( "%s\n%s\n", "Some formatted text.", "More text!" );
		System.out.printf( "%s\n%s\n%s\n%d\n", "John Smith", 
				"3000 Mission College Blvd", "Santa Clara, Ca", 95054 );
		//typically have zip as a string 
		
		
} //end main method 

} //end class 
